function z=GTRI_ze(n,sl,a)

if ( now-1 > 999999 ), ddd( 999999 ), exit, end

if (n==1) 

  z = 0.9858302 - 0.0333885*sl(1) + 0.00014064*sl(2) + 0.0000019*sl(3) + ...
      0.00000001*sl(4);

elseif (n==2) 

  z = 2.00337487 - 0.01141548*sl(1) + 0.0004159*sl(2) + 0.00000373*sl(3) + ...
      0.00000001*sl(4);

elseif (n==3) 

  z = 3.00636321 - 0.00683394*sl(1) + 0.00029281*sl(2) + 0.00000161*sl(3);

elseif (n==4)

  z = 4.00518423 - 0.00501795*sl(1) + 0.00021735*sl(2) + 0.00000088*sl(3);

else

 z = sqrt(a^2 + n^2);

end
